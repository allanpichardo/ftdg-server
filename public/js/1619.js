let map;
let socket;
let poly;

function start() {
    const bounds = {
        west: -95.912506,
        north: 45.930668,
        east: 19.721023,
        south: -30.0890
    };

    map = new google.maps.Map(document.getElementById('map'), {
        center: {lat: 20.588594, lng: -43.5690117},
        zoom: 3.7,
        styles: mapStyle,
        disableDefaultUI: true,
        restriction: {
            latLngBounds: bounds,
            strictBounds: false
        }
    });

    poly = new google.maps.Polyline({
        strokeColor: '#000000',
        strokeOpacity: 0.8,
        strokeWeight: 1
    });
    poly.setMap(map);

    socket = io('//ftdg.allanpichardo.com');

    socket.on('draw line', function(line){
        console.log('Received path');
        let start = new google.maps.LatLng(line.start);
        let end = new google.maps.LatLng(line.start);
        animatePath(start, end);
    });

}

function testLines() {
    setTimeout(() => {
        socket.emit('segment', 'Colombia,Cuba');
        setTimeout(() => {
            socket.emit('segment', 'Cuba,Dominican Republic');
            setTimeout(() => {
                socket.emit('segment', 'Dominican Republic,Puerto Rico');
                setTimeout(() => {
                    socket.emit('segment', 'Puerto Rico,Kongo');
                }, 1000);
            }, 1000);
        }, 1000);
    }, 1000);
}

function animatePath(start, end, durationSeconds = 1) {
    let line = new google.maps.Polyline({
        path: [start, start],
        strokeColor: "#222222",
        strokeOpacity: 1,
        strokeWeight: 1,
        geodesic: true, //set to false if you want straight line instead of arc
        map: map,
    });
    let step = 0;
    const numSteps = 60 * durationSeconds;

    let animationSequence = () => {
        step += 1;
        if (step < numSteps) {
            let are_we_there_yet = google.maps.geometry.spherical.interpolate(start,end,step/numSteps);
            line.setPath([start, are_we_there_yet]);
            requestAnimationFrame(animationSequence);
        }
    };
    requestAnimationFrame(animationSequence);
}
