let map;
let socket;
let poly;

function start() {
    const bounds = {
        west: -95.912506,
        north: 45.930668,
        east: 19.721023,
        south: -30.0890
    };

    map = new google.maps.Map(document.getElementById('map'), {
        center: {lat: 20.588594, lng: -43.5690117},
        zoom: 3.7,
        styles: mapStyle,
        disableDefaultUI: true,
        restriction: {
            latLngBounds: bounds,
            strictBounds: false
        }
    });

    poly = new google.maps.Polyline({
        strokeColor: '#000000',
        strokeOpacity: 1.0,
        strokeWeight: 3
    });
    poly.setMap(map);

    socket = io('//ftdg.allanpichardo.com/ws');

    socket.on('draw line', function(line){
        let path = poly.getPath();
        path.push(new google.maps.LatLng(line.start));
        path.push(new google.maps.LatLng(line.end));
    });

}

